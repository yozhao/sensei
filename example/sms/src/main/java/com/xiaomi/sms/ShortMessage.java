package com.xiaomi.sms;

public class ShortMessage {
  public long id;

  public long getId() {
    return this.id;
  }

  public int userId;

  public int getUserId() {
    return this.userId;
  }

  public String msgId;

  public String getMsgId() {
    return this.msgId;
  }

  public long msgTime;

  public long getMsgTime() {
    return this.msgTime;
  }

  public String contents;

  public String getContents() {
    return this.contents;
  }
}
